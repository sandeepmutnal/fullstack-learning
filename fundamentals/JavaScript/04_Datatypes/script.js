// Day 4: Data Types

// String
let name = "Vinsan";

// Number
let age = 21;

// Boolean
let isStudent = true;

// Null
let futureGoal = null;

// Undefined
let marks;

// Object
let person = {
  name: name,
  age: age,
  isStudent: isStudent
};

// Array
let subjects = ["Math", "Science", "English"];

// Output
console.log("Name:", name);
console.log("Age:", age);
console.log("Student:", isStudent);
console.log("Future Goal:", futureGoal);
console.log("Marks:", marks);
console.log("Person:", person);
console.log("Subjects:", subjects);